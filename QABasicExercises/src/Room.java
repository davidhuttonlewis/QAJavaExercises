
public class Room {

	private float size;
	private float coats;

	public Room(float size, float coats) {

		this.size = size;
		this.coats = coats;

	}

	public float getSize() {
		return size;
	}

	public void setSize(float size) {
		this.size = size;
	}

	public float getCoats() {
		return coats;
	}

	public void setCoats(float coats) {
		this.coats = coats;
	}

}
